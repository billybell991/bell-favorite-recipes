def get_best_photo(title, used_photos):
    """Find the best matching photo for a recipe title using priority matching."""
    title_lower = title.lower()

    # Sort keywords by length (longest first) → most specific match wins
    sorted_keywords = sorted(PHOTO_POOLS.keys(), key=len, reverse=True)

    best_pool = None
    for keyword in sorted_keywords:
        if len(keyword) <= 3:
            pattern = r'\b' + re.escape(keyword) + r'\b'
            if re.search(pattern, title_lower):
                best_pool = PHOTO_POOLS[keyword]
                break
        else:
            if keyword in title_lower:
                best_pool = PHOTO_POOLS[keyword]
                break

    if best_pool is None:
        best_pool = GENERIC_FOOD

    # Pick least-used photo from pool
    pool_with_counts = [(pid, used_photos.get(pid, 0)) for pid in best_pool]
    random.shuffle(pool_with_counts)
    pool_with_counts.sort(key=lambda x: x[1])

    chosen = pool_with_counts[0][0]
    used_photos[chosen] = used_photos.get(chosen, 0) + 1
    return get_url(chosen)


def main():
    recipes_dir = os.path.join(os.path.dirname(__file__), "content", "recipes")
    if not os.path.isdir(recipes_dir):
        print(f"Recipes directory not found: {recipes_dir}")
        return

    files = sorted([f for f in os.listdir(recipes_dir) if f.endswith('.md')])
    print(f"Found {len(files)} recipe files")

    updated = 0
    skipped = 0
    used_photos = {}

    random.seed(42)

    for filename in files:
        filepath = os.path.join(recipes_dir, filename)
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()

        # Extract title
        title_match = re.search(r'^title:\s*"(.+?)"', content, re.MULTILINE)
        if not title_match:
            skipped += 1
            continue

        title = title_match.group(1)

        # Skip _index.md or non-recipe files
        if title in ("All Recipes",):
            skipped += 1
            continue

        # Generate best image URL
        image_url = get_best_photo(title, used_photos)

        # Replace or add image field
        if re.search(r'^image:\s*".*?"', content, re.MULTILINE):
            new_content = re.sub(
                r'^image:\s*".*?"',
                f'image: "{image_url}"',
                content,
                count=1,
                flags=re.MULTILINE
            )
        elif '---' in content:
            # Add image field before closing ---
            parts = content.split('---', 2)
            if len(parts) >= 3:
                new_content = parts[0] + '---' + parts[1].rstrip() + f'\nimage: "{image_url}"\n---' + parts[2]
            else:
                new_content = content
        else:
            new_content = content

        if new_content != content:
            with open(filepath, 'w', encoding='utf-8') as f:
                f.write(new_content)
            updated += 1
            print(f"  ✓ {title}")
        else:
            skipped += 1

    unique_photos = len(used_photos)
    print(f"\nDone! Updated: {updated}, Skipped: {skipped}")
    print(f"Unique photos used: {unique_photos}")


if __name__ == '__main__':
    main()
